def add(n1, n2): return n1 + n2
def subtract(n1, n2): return n1 - n2

value1 = int(input('Number 1: '))
value2 = int(input('Number 2: '))
print(add(value1, value2))
print(subtract(value1, value2))

def multiply(n3, n4): return n3 * n4

value3 = int(input('Number 3: '))
value4 = int(input('Number 4: '))
print(multiply(value3, value4))

